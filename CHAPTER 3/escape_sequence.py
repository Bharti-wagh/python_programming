############################################## Newline (\n) ###################################################################
text="Today is good day but...\n it will make great day to do things on your interest."
print(text)

############################################## Tab (\t) #################################################################

print("Name\tAge")

######################################### Quotes inside quotes ###########################################################

print("She said \"Hello!\"") #Output: She said "Hello!"

######################################### Backslash (\\) ##################################################################

print("C:\\Users\\Bharti") 
# Output: C:\Users\Bharti

print(r"C:\Users\Bharti")
# Output: C:\Users\Bharti