'''
A SPAM COMMENT IS DEFINE AS A TEXT CONTAINING THE FOLLOWING KEYWORD :
"MAKE A LOT OF MONEY","BUY NOW","SUSCRIBE THIS","CLICK THIS"
WAP TO DETECT THESE SPAM
'''
TEXT=input("ENTER TEXT HERE : ")

if (TEXT=="MAKE A LOT OF MONEY" or TEXT=="BUY NOW" or TEXT=="CLICK THIS"):
    print("THESE ARE SPAM WORDS")
else:
    print("NOT SPAM WORDS")